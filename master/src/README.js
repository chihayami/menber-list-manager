/**
 * @file README.js
 *
 * 名簿更新システム (master)
 *
 * LINE Webhookからのメッセージに応じて会員名簿スプレッドシートを更新し, LINEへ自動返信するシステムである.
 *
 * 1. ファイル構成
 * - config.js : 定数および設定定義ファイル.
 * - log.js : 実行ログ記録およびスプレッドシート一括書き込み処理.
 * - process.js : スプレッドシート更新（昇級, 区分変更, 休退会, 入会）処理ロジック.
 * - webhook.js : LINE Webhook の受信およびコマンド解析, LINE返信処理.
 *
 * 2. 初期設定手順
 * - スクリプトプロパティ「LINE_TOKEN」にLINEのチャネルアクセストークンを設定する.
 * - GASエディタから「ウェブアプリ」としてデプロイし, Webhook URLを取得する.
 * - LINE Developersコンソールの「Webhook URL」に設定する.
 *
 * 3. 実行ログについて
 * スプレッドシート内の「実行ログ」シートに自動で処理ログが出力される. LINEで反応がない場合はここを確認する.
 */
