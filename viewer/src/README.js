/**
 * @file README.js
 *
 * 名簿ビューアシステム (viewer)
 *
 * 名簿更新システム (master) のスプレッドシートからデータを取得し, 事務作業で参照およびコピペするためのビューアシステムである.
 *
 * 1. ファイル構成
 * - config.js : 定数および設定定義ファイル.
 * - sync.js : masterからのデータ同期ロジックおよび自動同期トリガー管理.
 * - generator.js : コピペ用テキストの生成ロジック.
 * - コード.js : スプレッドシートのメニュー追加やシートの初期化などの制御フロー.
 * - clipboard.html : クリップボードへコピーするためのモーダルダイアログ用HTML.
 * - sidebar.html : 操作パネルとなるサイドバーUI用HTML.
 *
 * 2. 初期設定手順
 * - config.js の SPREADSHEET_ID にマスター名簿のスプレッドシートIDを設定する.
 * - スプレッドシートを開き, メニューの「名簿ビューア」 > 「【管理者用】自動同期トリガーを設定」を実行する.
 * - 初回実行時はGoogleアカウントの認証認可を承認する.
 *
 * 3. 主要機能
 * - 深夜の自動同期（毎日午前4時〜5時の間）.
 * - スプレッドシートのメニューやサイドバーからのコピペ用名簿のプレビューコピー.
 * - フィルターソート順でのコピー機能.
 */
